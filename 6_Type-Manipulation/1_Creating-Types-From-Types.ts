// Type Manipulation 类型处理

/**
 * Creating Types from Types 从类型创建类型
 * TypeScript 的类型系统非常强大，因为它允许根据其他类型来 表示 类型。
 * 这个想法的最简单形式是泛型，我们实际上有各种各样的类型运算符可供我们使用。也可以根据我们已有的值来表示类型。
 * 通过组合各种类型运算符，我们可以以简洁、可维护的方式表达复杂的操作和值
 * 
 * Generics - 带参数的类型
 * Keyof Type Operator - 使用 keyof 运算符创建新类型
 * Typeof Type Operator - 使用 typeof 运算符创建新类型
 * Indexed Access Types - 使用 Type['a'] 语法访问类型的子集
 * Conditional Types -  类型类似于类型系统中的 if 语句
 * Mapped Types - 通过映射现有类型中的每个属性来创建类型
 * Template Literal Types - 通过模板文字字符串更改属性的映射类型
 */
